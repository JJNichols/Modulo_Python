#Operadores Relacionais

x = 5;
y = 7;

#Maior que
print(x > y); #False
print();

#Menor que
print(x < y); #True
print();

#Igualdade
print(x == y); #False
print();

#Maior ou igual a
print (x >= y); #False
print();

#Menor ou igual a
print(x <= y); #True
print();

#Differente de
print(x != y); #True
print();



